export function getCountryImageTopics(country) {
  const configured = COUNTRY_IMAGE_TOPICS[country?.code];
  if (configured?.length) return configured;
  return [`${country?.name ?? ""} landmark`, `${country?.name ?? ""} capital city`].filter(Boolean);
}

export function getCountryImageOverrideUrl(country) {
  return COUNTRY_IMAGE_OVERRIDES[country?.code] ?? null;
}
